var account = "";

async function connectWallet()
{
    if(window.ethereum)
    {
        ethereum.request({ method: 'eth_requestAccounts' }).then(accounts=>{
        let boutonWallet = document.getElementById("connectWallet_bouton");
        let connectWallet_txt = document.getElementById("connectWallet_txt");

        account = accounts[0];
        console.log(account);
        let walletNumber = document.createElement("h3");
        walletNumber.style.width = "150px";
        walletNumber.style.overflow = "hidden";
        walletNumber.style.textOverflow = "ellipsis";
        walletNumber.textContent = account;

        boutonWallet.removeAttribute("onclick");
        connectWallet_txt.textContent = "Wallet Connected !";
        boutonWallet.append(walletNumber);

        getNumberOfNfts();

        //activation bouton mint
        let mint_bouton = document.getElementById("mint_bouton");
        mint_bouton.style.cursor = "pointer";
        mint_bouton.setAttribute("onclick","mint()");
        });
    }
    else
    {
        let boutonWallet = document.getElementById("connectWallet_bouton");
        let connectWallet_txt = document.getElementById("connectWallet_txt");

        boutonWallet.style.width = "400px";
        connectWallet_txt.textContent = "Please install metamask first";
    }
}

async function mint()
{
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = await provider.getSigner();

    const erc721Contract = new ethers.Contract(erc721Address, erc721ABI, signer); 
    await erc721Contract.safeMint(account, {value: ethers.utils.parseEther("0.01")});
}

async function getNumberOfNfts()
{
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const erc721Contract = new ethers.Contract(erc721Address, erc721ABI, provider); 
    let nb = await erc721Contract.totalSupply();
    
    console.log(nb);
    let txt_info = document.getElementById("txt_info");
    txt_info.textContent = "There is currently " + nb + "/10 minted Nft(s)";
}

